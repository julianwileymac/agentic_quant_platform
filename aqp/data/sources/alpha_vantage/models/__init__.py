"""Flexible Pydantic models for Alpha Vantage responses.

Alpha Vantage frequently adds/removes fields without versioning. These models
intentionally allow extras while giving the API and web UI stable class names.
"""
from __future__ import annotations

from typing import Any

from pydantic import BaseModel, ConfigDict

from aqp.data.sources.alpha_vantage._parsers import normalize_mapping


class AVModel(BaseModel):
    model_config = ConfigDict(extra="allow", populate_by_name=True)

    @classmethod
    def model_validate(cls, obj: Any, *args: Any, **kwargs: Any):  # type: ignore[override]
        if isinstance(obj, dict):
            obj = normalize_mapping(obj)
        return super().model_validate(obj, *args, **kwargs)


class AVResponse(AVModel):
    data: dict[str, Any] | list[Any] | None = None


class AVMetadata(AVModel):
    information: str | None = None
    symbol: str | None = None
    last_refreshed: str | None = None
    output_size: str | None = None
    time_zone: str | None = None


class ResponseStatus(AVModel):
    ok: bool = True
    message: str | None = None


class TimeSeriesPayload(AVModel):
    metadata: dict[str, Any] = {}
    bars: list[dict[str, Any]] = []


class OhlcvBar(AVModel):
    timestamp: str | None = None
    open: float | None = None
    high: float | None = None
    low: float | None = None
    close: float | None = None
    adjusted_close: float | None = None
    volume: float | None = None


class GlobalQuote(AVModel):
    symbol: str | None = None
    open: float | None = None
    high: float | None = None
    low: float | None = None
    price: float | None = None
    volume: float | None = None
    latest_trading_day: str | None = None
    previous_close: float | None = None
    change: float | None = None
    change_percent: str | None = None


class SymbolSearchMatch(AVModel):
    symbol: str | None = None
    name: str | None = None
    type: str | None = None
    region: str | None = None
    market_open: str | None = None
    market_close: str | None = None
    timezone: str | None = None
    currency: str | None = None
    match_score: str | None = None


class MarketStatusEntry(AVModel):
    market_type: str | None = None
    region: str | None = None
    primary_exchanges: str | None = None
    local_open: str | None = None
    local_close: str | None = None
    current_status: str | None = None


class MarketStatusPayload(AVModel):
    markets: list[MarketStatusEntry] = []


